  startDate.setHours(0, 0, 0, 0);
  endDate.setHours(0, 0, 0, 0);

  return forecastData.value.filter(day => {
    const dayDate = new Date(day.time);
    dayDate.setHours(0, 0, 0, 0);
    return dayDate >= startDate && dayDate <= endDate;
  });
});

